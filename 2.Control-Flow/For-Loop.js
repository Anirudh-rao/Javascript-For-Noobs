/*
In programming, loops are used to repeat a block of code.
*/

const n = 5;

//Looping from i=1 to 5
for(let i=1; i<=n ;i++){
    console.log("I Love Javascript");
}

// looping from i = 1 to 5
// in each iteration, i is increased by 1
for (let i = 1; i <= n; i++) {
    console.log(i);     // printing the value of i
}